import Foundation
import UIKit

extension UIColor {
    
    static var blueColor: UIColor {
        return UIColor(named: "blueColor")!
    }
    
    static var whiteColor: UIColor {
        return UIColor(named: "whiteColor")!
    }
    
    static var coolColor: UIColor {
        return UIColor(named: "coolColor")!
    }
    
    static var charcoalColor: UIColor {
        return UIColor(named: "charcoalColor")!
    }
    
}
